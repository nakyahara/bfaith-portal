// ─── インポート処理 ───

const monthlyData = new Map();

// 売上データをyear_monthでグループ化（Qoo10はクーポン追跡なし、O列が既に純売上）
for (const [ym, seg, sales, cost] of salesHistorical) {
  if (!monthlyData.has(ym)) {
    monthlyData.set(ym, { bySegment: {}, pfFee: 0, adCost: 0 });
  }
  const d = monthlyData.get(ym);
  d.bySegment[String(seg)] = {
    売上合計: sales,
    原価合計: cost,
    行数: 0,
    原価率: sales > 0 ? (cost / sales * 100).toFixed(1) : '0.0',
  };
}

// PF手数料・広告費をマージ
for (const [ym, pfFee, adCost] of feeHistorical) {
  if (monthlyData.has(ym)) {
    monthlyData.get(ym).pfFee = pfFee;
    monthlyData.get(ym).adCost = adCost;
  } else {
    monthlyData.set(ym, { bySegment: {}, pfFee, adCost });
  }
}

// DBに挿入
const insertStmt = db.prepare(`INSERT OR REPLACE INTO mart_qoo10_monthly_summary
  (year_month, total_rows, resolved_count, unresolved_count,
   by_tax, by_segment, excluded, mf_row, pf_fee, ad_cost, confirmed_at)
  VALUES (?,?,?,?,?,?,?,?,?,?,?)
`);

const now = new Date().toISOString().replace('T', ' ').slice(0, 19);
let imported = 0;
let skipped = 0;

const insertMany = db.transaction(() => {
  for (const [ym, data] of [...monthlyData.entries()].sort()) {
    const totalSales = Object.values(data.bySegment).reduce((s, v) => s + (v.売上合計 || 0), 0);
    if (totalSales === 0 && data.pfFee === 0 && data.adCost === 0) {
      skipped++;
      continue;
    }

    const totalCost = Object.values(data.bySegment).reduce((s, v) => s + (v.原価合計 || 0), 0);

    // 全売上を10%扱い（過去データは税率内訳なし）
    const byTax = {
      '10': {
        売上合計: totalSales,
        原価合計: totalCost,
        行数: 0,
      },
      '8': { 売上合計: 0, 原価合計: 0, 行数: 0 },
    };

    const mfRow = {
      '商品売上(10%)': Math.round(totalSales * 1.1),
      '商品売上(8%)': 0,
      '合計': Math.round(totalSales * 1.1),
    };

    const excluded = { '4': { 売上合計: 0, 原価合計: 0, 行数: 0 } };

    insertStmt.run(
      ym, 0, 0, 0,
      JSON.stringify(byTax),
      JSON.stringify(data.bySegment),
      JSON.stringify(excluded),
      JSON.stringify(mfRow),
      data.pfFee, data.adCost, now + ' [historical]'
    );
    imported++;
  }
});

insertMany();

